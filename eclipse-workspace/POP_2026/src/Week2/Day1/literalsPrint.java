package Week2.Day1;

public class literalsPrint {
    public static void main(String[] args) {
        // 1. Number literal (Integer)
        System.out.println(100); 

        // 2. Decimal literal (Floating-point)
        System.out.println(12.5); 

        // 3. Character literal (Single character in single quotes)
        System.out.println('A'); 

        // 4. String literal (Text in double quotes)
        System.out.println("Hello, Java!"); 

        // 5. Boolean literal (True or False)
        System.out.println(true); 
    }
}